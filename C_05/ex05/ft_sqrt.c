#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 0)
		return (0);
	while (nb / i > i)
	{
			i++;
		if ((nb % i) == 0)
			return (i);
	}
	return (0);
}
