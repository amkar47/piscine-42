int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned	i;

	i = 0;
	while (1 < n - 1 && s1[i] && s2[i])
	{
		if (n == 0)
			return (0);
		i++;
		while (s1[i] == s2[i] && s1[i] != 0 && s2[i] != 0)
		{
			i++;
			if (s1[i] != s2[i])
				return (s1[i] - s2[i]);
		}
	}
	return (s1[i] - s2[i]);
}
